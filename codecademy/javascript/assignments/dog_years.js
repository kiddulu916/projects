// Setting my age to a variable
var myAge = 34

// Setting variable for the first 2 years of life
let earlyYears = 2

// Converting the first 2 years of life to dog years
earlyYears = earlyYears * 10.5

// Setting a variable for the later years after 2
let laterYears = myAge - 2

// converting the later years of life to dog years
laterYears = laterYears * 4

// Calculating my age in dog years
var myAgeInDogYears = earlyYears + laterYears

// Setting string of my name to a variable
var myName = 'Corey Scott Hilsenbeck'.toLowerCase()

// Interpolate my name and age in dog years
console.log(`My name is ${myName}. I am ${myAge} years old in human years which is ${myAgeInDogYears} years old in dog years.`)